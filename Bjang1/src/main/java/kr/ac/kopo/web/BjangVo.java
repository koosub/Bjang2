package kr.ac.kopo.web;

public class BjangVo {
	
	private String addressid;
	private int year;
	private int month;
	private int total;
	private int men;
	private int women;
	
	public String getAddressid() {
		return addressid;
	}
	public void setAddressid(String addressid) {
		this.addressid = addressid;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public int getMen() {
		return men;
	}
	public void setMen(int men) {
		this.men = men;
	}
	public int getWomen() {
		return women;
	}
	public void setWomen(int women) {
		this.women = women;
	}
	
	
	
	
}
