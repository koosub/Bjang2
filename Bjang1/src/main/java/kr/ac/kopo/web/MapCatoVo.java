package kr.ac.kopo.web;

public class MapCatoVo {
	
	private String sinm;
	private int difVal;
	private int curVal;
	private double lat;
	private double lng;
	
	public String getSinm() {
		return sinm;
	}
	public void setSinm(String sinm) {
		this.sinm = sinm;
	}
	public int getDifVal() {
		return difVal;
	}
	public void setDifVal(int difVal) {
		this.difVal = difVal;
	}
	public int getCurVal() {
		return curVal;
	}
	public void setCurVal(int curVal) {
		this.curVal = curVal;
	}
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public double getLng() {
		return lng;
	}
	public void setLng(double lng) {
		this.lng = lng;
	}
	
	
	
	
	
	
}
