package kr.ac.kopo.web;


public class NumOfInfVo {
	
	private String sinm;
	private String epidemicnm;
	private String age;
	private int woman;
	private int man;
	private double total;
	private int year;
	private int month;
	private int sum;
	
	
	public String getSinm() {
		return sinm;
	}
	public void setSinm(String sinm) {
		this.sinm = sinm;
	}
	public String getEpidemicnm() {
		return epidemicnm;
	}
	public void setEpidemicnm(String epidemicnm) {
		this.epidemicnm = epidemicnm;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public int getWoman() {
		return woman;
	}
	public void setWoman(int woman) {
		this.woman = woman;
	}
	public int getMan() {
		return man;
	}
	public void setMan(int man) {
		this.man = man;
	}
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	
	
	
	
}
