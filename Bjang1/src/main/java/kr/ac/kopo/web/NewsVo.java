package kr.ac.kopo.web;

public class NewsVo {

	private int year;
	private int month;
	private String districtnm;
	private String epidemicnm;
	private int volume;
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public String getDistrictnm() {
		return districtnm;
	}
	public void setDistrictnm(String districtnm) {
		this.districtnm = districtnm;
	}
	public String getEpidemicnm() {
		return epidemicnm;
	}
	public void setEpidemicnm(String epidemicnm) {
		this.epidemicnm = epidemicnm;
	}
	public int getVolume() {
		return volume;
	}
	public void setVolume(int volume) {
		this.volume = volume;
	}
	
	
}
