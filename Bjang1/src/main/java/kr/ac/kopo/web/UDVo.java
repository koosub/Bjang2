package kr.ac.kopo.web;


public class UDVo {
	
	private String infdisnm;
	private String infdnm;
	private int sum;
	private int total;
	private int ssum;
	private String definition;
	private String symptom;
	private String relevant;
	
	public String getInfdisnm() {
		return infdisnm;
	}
	public void setInfdisnm(String infdisnm) {
		this.infdisnm = infdisnm;
	}
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public String getInfdnm() {
		return infdnm;
	}
	public void setInfdnm(String infdnm) {
		this.infdnm = infdnm;
	}
	public int getSsum() {
		return ssum;
	}
	public void setSsum(int ssum) {
		this.ssum = ssum;
	}
	public String getDefinition() {
		return definition;
	}
	public void setDefinition(String definition) {
		this.definition = definition;
	}
	public String getSymptom() {
		return symptom;
	}
	public void setSymptom(String symptom) {
		this.symptom = symptom;
	}
	public String getRelevant() {
		return relevant;
	}
	public void setRelevant(String relevant) {
		this.relevant = relevant;
	}
	
	
	
}
